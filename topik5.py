# topik 5 : if else, if if, if elif

number = input("choose any number : ")
number = int(number)

if number > 10:
  print(f"{number} is greater than 10")
if number > 20:
  print(f"{number} is greater than 20")
if number > 5:
  print(f"{number} is greater than 5")
elif number < 5:
  print(f"{number} is less than 5")
elif number < 2:
  print(f"{number} is less than 2")
else:
  print("unknown")

